package tisse.model;

public class DeptOld {

    private Integer id;
    private String name;
    private String firmLink;
    private String departNum;
    private String timeZone;
    private String description;
    private String guid1c;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFirmLink() {
        return firmLink;
    }

    public void setFirmLink(String firmLink) {
        this.firmLink = firmLink;
    }

    public String getDepartNum() {
        return departNum;
    }

    public void setDepartNum(String departNum) {
        this.departNum = departNum;
    }

    public String getTimeZone() {
        return timeZone;
    }

    public void setTimeZone(String timeZone) {
        this.timeZone = timeZone;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getGuid1c() {
        return guid1c;
    }

    public void setGuid1c(String guid1c) {
        this.guid1c = guid1c;
    }
}
