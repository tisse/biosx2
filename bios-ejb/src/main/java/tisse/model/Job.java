package tisse.model;

public class Job {

    private Integer id;
    private String name;
    private String guid1c;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGuid1c() {
        return guid1c;
    }

    public void setGuid1c(String guid1c) {
        this.guid1c = guid1c;
    }
}
